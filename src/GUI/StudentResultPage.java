    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = rollNoTextField.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs =st.executeQuery("SELECT * FROM Student INNER JOIN result WHERE Student.rollNo ='"+rollNo+"' and result.rollNo='"+rollNo+"'");
                        
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"RollNo. doesn't exist");
            }
            else{
                
                studentName.setText(rs.getString(1));
                studentName.setEditable(false);
                
                genderTextField.setText(rs.getString(3));
                genderTextField.setEditable(false);
                
                fatherName.setText(rs.getString(4));
                fatherName.setEditable(false);
                
                courseText.setText(rs.getString(5));
                courseText.setEditable(false);
                
                branchText.setText(rs.getString(6));
                branchText.setEditable(false);
                
                englishText.setText(rs.getString(8));
                englishText.setEditable(false);
                
                physicsText.setText(rs.getString(9));
                physicsText.setEditable(false);
                
                chemistryText.setText(rs.getString(10));
                chemistryText.setEditable(false);
                
                mathsText.setText(rs.getString(11));
                mathsText.setEditable(false);
                
                computerText.setText(rs.getString(12));
                computerText.setEditable(false);
                
                totalMarks_textField.setText(rs.getString(13));
                totalMarks_textField.setEditable(false);
                
                int englishMarks = Integer.parseInt(rs.getString(8));
                int physicsMarks = Integer.parseInt(rs.getString(9));
                int chemistryMarks = Integer.parseInt(rs.getString(10));
                int mathsMarks = Integer.parseInt(rs.getString(11));
                int computerMarks = Integer.parseInt(rs.getString(12));
                
                if(englishMarks >=30 && physicsMarks>=30 && chemistryMarks>=30 && mathsMarks>=30 && computerMarks>=30){
                    verdictResult_textField.setText("PASS");
                }
                else{
                    verdictResult_textField.setText("FAIL");
                }
                verdictResult_textField.setEditable(false);
            }
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

